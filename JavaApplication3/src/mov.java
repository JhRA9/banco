public class mov {
    String tipo;
    String fecha;
    Double monto;

    public mov() {
    }

    public mov(String tipo, String fecha, Double monto) {
        this.tipo = tipo;
        this.fecha = fecha;
        this.monto = monto;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public Double getMonto() {
        return monto;
    }

    public void setMonto(Double monto) {
        this.monto = monto;
    }
}